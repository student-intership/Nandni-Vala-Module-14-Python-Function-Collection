import math

number1 = 16
number2 = 4.7

square_root = math.sqrt(number1)
print(f"The square root of {number1} is: {square_root}")

rounded_up = math.ceil(number2)
print(f"The ceiling of {number2} is: {rounded_up}")

rounded_down = math.floor(number2)
print(f"The floor of {number2} is: {rounded_down}")
